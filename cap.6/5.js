// Definição da função
function calcularMedia(num1, num2, num3) {
    // Calcula a média
    let media = (num1 + num2 + num3) / 3;

    // Exibe o resultado
    console.log("A média dos números é:", media);
}

// Chamada da função com três números de exemplo
calcularMedia(5, 10, 15);
